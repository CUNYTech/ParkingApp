class User {
    public int userName;
    public int passWord;

    public void setUserName(int userName) {
        this.userName = userName;
    }
    public void setPassWord(int passWord) {
        this.passWord = passWord;
    }

    public int getUserName() {
        return userName;
    }

    public int getPassWord() {
        return passWord;
    }
}
